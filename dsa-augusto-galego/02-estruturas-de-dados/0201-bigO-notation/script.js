// Big-O notation
// Fala sobre escabilidade
// Forma de denotar desempenho
// Nao eh necessariamente uma medida de performance

// Fala o quao o algoritmo vai "escala" com o tamanho do input
// Nao necessariamente sobre a performance do algoritmo

// Pode ser usado pra medir complexidade temporal e a complexidade espacial
// Complexidade temporal => tempo de execucao / runtime
// Complexidade Espacial => o quanto de memoria adicional precisamos alocar

// Big O eh uma denotacao que vai dizer quao bem ou quao mal o algoritmo escala dado o tamanho de um input

// Principais big-o notations
// Melhor para pior
// O(1) -> tempo constante / memoria constante
// O(LOG n) -> Binary tree | Conforme o input cresce muito rapido, o tempo de execucao nao aumenta tao rapido quanto
// O(n) -> Escala exatamente na mesma medida em que o input aumenta
// O(n LOG n) -> Sorting (quicksort, mergesort), Divide and Conquer | Percorre todos os elementos varias vezes
// O(n^2) -> quando tem for dentro de for, ou em Bubble sort.

// MUITO RAROS:
